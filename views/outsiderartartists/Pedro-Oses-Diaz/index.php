			<div class="sectiontitle" id="artistssectiontitle">Pedro Oses Diaz

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Villa Clara,&nbsp;1954 &ndash; 2009

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Pedro Oses was born in Guaracabulla, a small town that was his entire universe and where he developed his whole work until his death in 2009. At nine years old, the doctors found and removed a tumor in his spine. The disease limited his normal growth and caused him problems with the muscular control of his extremities, thus he lived with a permanent physical disability. After this incident, he started painting. He later commented, he began painting because he got bored at home as he couldn&rsquo;t go to school or help his family with the farm work. In 1979, he met the Cuban writer and painter Samuel Feij&oacute;o and the group Signos (Signs), that Feij&oacute;o managed. This group was formed by singular self-taught artists from the central provinces of Cuba. Pedro instantly became an inseparable and faithful exponent of that group. Although he didn&rsquo;t finish elementary school nor he had an artistic education, the great originality of his works and his virtuosity astonished the specialists. Many of his works use the countryside as motif and are influenced by traditions, myths, legends and superstitions that he reflected in a unique way, where reality overlaid with fantasy. In other cases, he recreates a completely illusive, mystical, enigmatic and sometimes chilling world. This takes him apart from folk, popular and na&iuml;ve art. His works show his philosophy of life, love, religion and death through a relevant and fabulous set of symbols he uses to express different meanings. Painting was his way of designing his own universe through forms and colors, fusing common and singular subjects. He is considered one of the most important Cuban self-taught painters.<br/>
</p>
