			<div class="sectiontitle" id="artistssectiontitle">Ramon Moya Hernandez 

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Guantanamo, 1950

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>He was born in a peasant and poor family. At the age of 14 he went to Havana and he began to study plumbing. He began to work at different occupations: mechanic at a workshop, lathe operator, at construction sites, longshoreman, etc. After several years living in Havana, he returned to his natal province. In 1984, he began his first artworks, completely self-taught in art, creating sculptures from wooden pieces that he collected at the streets. These first images he carves, come in dreams in a repeated and uninterrupted way. This is how a new stage in his life aroused, which no longer will be close. His works was acknowledged by personalities of the local culture of his province and he was invited to take part in different exhibitions and contests. The principal sources of inspiration for his work are the politic and social problems. His way of life is completely irreverent: he has not been afraid to walk the streets naked or dressed as the prisoners at the Guantanamo US military naval base. He frequently creates his own clothes, most of them truly pieces of art.  Most of the time he lives at the mountains and forests of his region, inside natural caves as his only shelter. When he needs to do something, he always struggles to achieve his objective, although the materials at hand could not be the most appropriated ones. For him, materials are just a way to reach the idea that hovers around in his head and which he needs to express, whatever element he has to do it. <br/></p>
Main Exhibitions<br/></p>
1990. The sculptured object. Center for the Development of Visual Arts. Havana, Cuba. <br/></p>
1989. 3rd Havana Biennial. National Museum of Fine Arts. Havana, Cuba. <br/></p>
1987. Second Hall of Prizewinners. National Museum of Fine Arts. Havana, Cuba. <br/></p>
1987. Moya - non imperialistic. Wilfredo Lam Gallery. Havana, Cuba.<br/></p>
1986. 22 sculptures in wood and one painting. Municipal Gallery. Guantanamo, Cuba. <br/></p>
1986. First Hall of the City. Municipal Gallery. Guantanamo, Cuba. <br/></p>
