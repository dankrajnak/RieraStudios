			<div class="sectiontitle" id="artistssectiontitle">Federico Garcia Cortizas

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Havana, 1971

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Federico lost his father when he was two years old. His mother took care of his education and always support his approach to painting and other arts, like music, literature and cinema, although he never took any specialized art training. During his youth he had a strong character and found his own shelter painting and making wood sculptures, using nature as an inspiration to express himself. Federico defines himself as a wood carver, a movie projectionist, a maintenance worker, a sentinel for urban hydroponic and a construction worker. However, he doesn&rsquo;t define himself as a painter or a sculptor despite his undeniable skills. Federico designs and builds his own tools. He also likes to collect or accumulate pieces of woods and rocks. He really worries about nature and he suffers from excessive tree cutting and pollution. He states he has never cut a tree to create his wood sculptures. His drawings recreate a world of utopian landscapes where trees domain every inch of the space. There is life, strength, sensuality and darkness all over his works. He also comments in some cases wood talks to him.<br/></p>
