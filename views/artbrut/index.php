		ART BRUT PROJECT CUBA
		</div>
		<div class="sectionbody" id="artbrutsectionbody">
			<p>
			Art Brut Project Cuba is an art project created by RIERA STUDIO with the purpose of promoting and supporting singular art expressions, that share a stylistic distance from the existing visual culture based on academic or intellectual training; and whose makers have no knowledge of intellectual artistic issues. Art made within this context and usually gathered under the related categories of Art Brut and Outsider Art, each one with its own conceptual characteristics but with dashing boundaries in between, are the scope of our project.<br><br>
			Art Brut was created as a term in the late 1940s by the French artist Jean Dubuffet to draw attention to a form of art with little or no connection with existing art styles. Its creators have not had any formal art training but express their personal experiences and deep inner feelings in a very obsessive way. They're frequently affected by mental disorders, traumatic experiences or social isolation. <br><br>
			Outsider Art begin to be used as an English synonym for Art Brut after the English art historian Roger Cardinal published his book of the same name. Over time, it became more inclusive to refer not only to the art production of people living in extremes mental or social conditions but also to those self-taught and self-willed individuals who express themselves on the margin of stablished cultural mainstreams. <br><br>
			Art Brut Project Cuba is actually the only Cuban art project specialized in this forms of visual art.  Its main task is the acknowledgment and promotion of the Cuban creators of Art Brut and Outsider Art, and their artworks, in the national and international scenes. Our facilities included a reference center with available information about these artists and the conservation of most of their artworks. <br><br>
			Many of these artists have severe mental illness or difficult health conditions which in many cases are a burden for their families. Thus, one of our most important goals is their recognition by their families as artistically creative individuals and to help them to be more independent. <br><br>
			We also create workshops and offer work residencies to creators who for particular reasons cannot develop their artworks at their homes and without interfering in anyway with their creative processes. Our support to the artists within the project includes providing art materials and all the facilities to show and promote their works. Also, in the case of the workshops we provide foods for them. However, to maintain these actions we strongly depend on the funds donated to the project and the collaboration of other institutes, organizations and personal aids. 
			</p>
