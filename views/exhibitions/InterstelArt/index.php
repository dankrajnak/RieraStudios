<span> Words on catalogue by Derbis Campos Hernandez*</span>
<p>We are not alone. The universe is infinitely big to deny this statement. The existence of life beyond our Blue Planet is so undeniable even for the most skeptical people if we just scientifically look at it from a probabilistic view.</p>
<p>How are the inhabitants of other worlds? How could their scientific and technological development be? Have they visited our lands and seas since the beginning of humanity or even before, and could they continue doing it today? If so, what are their intentions? Are there human beings &ldquo;chosen&rdquo; to consciously and unconsciously notice their presence, voluntarily or involuntarily? They are all questions which have a fertile debate on an eager community for these issues. </p>
<p>Some of these people claim to have proof of these visits, which could be demonstrable or not; others only have their pure personal conviction based on actual or spiritual experiences or self-belief and finally, there are the ones who are scientifically skeptical but still not immune to such an interesting topic.</p>
<p>Undeniable for everyone is to appreciate the communicative, experiential and artistic value of the graphic works, either in the form of drawings or through the written word, that those people, whose close encounters of any kind are their motivation, express through their experiences and knowledge. </p>
<p>This is how we were fascinated by the mixture of colors and known, suggested, dreamed or unexplained forms that proposes Marco Antonio Guerrero in his drawings of technologically driven worlds; or by the unconsciously and obsessive need of Chucho to write down in any sheet-like material he has at hand his frequently non-terrestrial visits and travels; or finally, by the scenes of her isolated childhood countryside that Emili Tito Guerrero draw at just 5 years old. All these works help to make real a tiny portion of that vast interstellar space. Undoubtedly, this is an amazing experience of interchange and knowledge to followers and not followers of this controversial topic.</p>
<span>* Derbis Campos Hernandez is biochemist, photographer and General Assistant for RIERA STUDIO | Art Brut Project Cuba</span>
