<span> Words on catalogue by Carlos Javier Garcia Huergo (Text in Spanish)</span>
<p>Apto 11</p>
<p>669</p>
<p>(1) En todos los idiomas </p>
<p>a)</p>
<p>(1) Buenas madrugadas, tardes y noches </p>
<p>(b) Buenos d&iacute;as 31+13=44</p>
<p>(2) 31 alma francesa y cantidad de segmentos de mi bandera cubana, totales + unitarios 28 (alma inglesa) +3=31 </p>
<p>(3) 7 meses, 31 d&iacute;as </p>
<p>13 Am&eacute;n y simpat&iacute;as al gran muchachito Eli&aacute;n.</p>
<p>13 X (4) = 52 =) el 52 y el Am&eacute;rica =) ok</p>
<p>(4) Les transmito bendiciones de Dios a todos mis amigos (ver canci&oacute;n de Roberto Carlos) no digo cual. Y para el resto son sabias las palabras simpat&iacute;as.</p>
<p>Las cuales son a&ntilde;adidas por supuesto a los m&iacute;os (familia, amigos, etc.) </p>
<p>(5) Me gustar&iacute;a transmitirle las bendiciones de Dios de forma agradable a mi familia. Eso est&aacute; en proceso igual que el queso.</p>
<p>(6) Am&eacute;n muchas gracias a Dios que me envi&oacute; un &ldquo;paparazzi&rdquo; llamado cari&ntilde;osamente en el barrio Samuelito el cual es un gran artista.</p>
<p>II</p>
<p>(1) Mi humilde obra trata de simbiotizar dos palabras: </p>
<p>(A) sinergia: ahorro, econom&iacute;a, simpleza, eficiencia y cierta competitividad (la principal emulaci&oacute;n es con uno mismo, es la m&aacute;s eficiente) principio Baha&acute;i.</p>
<p>(B) Catarsis: fin u objetivo. Purgante de lo mal&eacute;fico. Mi arte para ello trata de ser piadoso, reflexivo, estimulante, autocr&iacute;tico y sobre todo simp&aacute;tico</p>
<p>(2) Por favor, ver regla de oro del cristianismo, que ya era popular en todo el oriente varios siglos antes de Cristo. </p>
<p>(3) Am&eacute;n por la piedad.</p>
<p>(4) y si hay cierta compasi&oacute;n en mi&hellip; que sea la proclamada por el Dalai Lamas escandinavo en el teatro infantil.</p>
<p>(5) Occidente desconoce la piedad y su compasi&oacute;n es &hellip; Sus resultados &hellip; es normal que el fastidiado quiera ver &hellip; a su pr&oacute;jimo &hellip;     </p>
<p>(6) Les doy por ello a esa energ&iacute;a un Am&eacute;n poco Venedito para dar un d&iacute;a un Am&eacute;n Benedicto.</p>
<p>III</p>
<p>Subt&iacute;tulos: Honras a mi mam&aacute; </p>
<p>(1) Mi pap&aacute;, sin comentarios &hellip; con su formaci&oacute;n austera debida a hecho de mi arte algo econ&oacute;mico consiente, que aprovecha las manchas, relieves. Algo eficiente apart&aacute;ndome (de las falsas modestias).</p>
<p>(2) Honro a toda mi casa, cuya principal lumbrera era Irenita quien qued&oacute; como una buena persona a pesar de que le falt&oacute; tener de Ti&ntilde;osa (ver &ldquo;Magia&rdquo; sin comentario no. 1)</p>
<p>(3) Adem&aacute;s ella ve&iacute;a con simpat&iacute;a incluso sus adversarios en las distintas disciplinas humanas, honrando la frase de Erick el samur&aacute;is: &ldquo;hay de quien le diga a lo blanco negro y a lo negro blanco&rdquo;. No obstante, ella simpatizaba con lo natural reflejado en la pintura, el cine, la fotograf&iacute;a y en el natural medio ambiente. </p>
<p>(4) Am&eacute;n por sus sabias leyes, las cuales no expondr&eacute; y cuyo conocimiento nos llevaron a muchos fracasos.</p>
<p>(5) Sabio distinguir excepciones de la simbolog&iacute;a colorar y acolorar. En mi caso hay tres trinidades, tres tetraidades, tres unidades (fe, esperanza y amor) e incontables dualidades. Am&eacute;n por las recepciones divinas cosidas (ver canciones de Van Van).</p>
<p>(6) Paciencia conmigo pues prefiero ser negro que un tiro al blanco.</p>
<p>(7) Premisa cubana: Tirar y tirar bien&hellip; </p>
<p>(8) Am&eacute;n por cambiar mi seud&oacute;nimo de Julio por Augusto.</p>
<p>(9) Gracias pero no como el brasile&ntilde;o Agust&iacute;&ntilde;o. Am&eacute;n lo siento y sorry.</p>
<p>&hellip;Papi dame 10 pesos, &hellip;ni&ntilde;o!!!!&hellip; para que t&uacute; quieres 5 pesos? si con dos te alcanzan, mira toma 1 y dale la mitad a tu hermana.</p>
