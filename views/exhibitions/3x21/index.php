<span> Words on catalogue by Dr. Marcelino Feal Suarez*</span>
<p>Art Brut is a term created in 1945 by Jean Dubuffet to describe, from an academic point of view, the productions of people without an artistic training and with very unique personalities reflected in their works.</p>
<p>Only in the more developed countries is there funding intended to promote this form of art. Luckily in Cuba, we already have the existence of Riera Studio, which in a very short time has given very solid steps and today, it comes at a very important moment in its history joining for the first time, three prominent artists of Cuban Art Brut, each one with their very own ways to create. Lien (1988) gives us his graphemes, which are harmonically compounded and full of love for her family. Darien (1984), who has several times survived severe health issues, shows us the artistic representation of the dream-like world where his mother submerged him, through the magic of stories. And Roberto (1961) exhibits his world of colors with a very particular perspective, reaching the gates of ultramodern 3D. </p>
<p>In a globalized world, where often the first thing you do when evaluating an artwork is to decide if it has commercial value or not, Art Brut goes in a totally different direction as these artists only create by the need they feel to find a communication channel with the surrounding environment; but none of them are able to put a price on their works, or to enter a bid auction. They are not even able to define such abstract concepts like money, market and economic welfare.</p>
<p>It would be great that each of these artists Samuel Riera has brought together in a single exhibition today, were able to tell us what they think about each other&rsquo;s work. And the saddest thing is that maybe they can fulfill this task, but then we would be the ones who could not decipher the message they would show us through babbling, visual codes or just by using lines and colors. And that would only prove once again how imperfect are humans who consider themselves normal.</p>
<span>* Marcelino Feal Suarez is doctor in internal medicine and surgeon  </span>
