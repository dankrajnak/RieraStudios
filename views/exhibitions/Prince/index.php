<span> Words on catalogue by Maria de los Angeles Matienzo*</span>
<p>Josvedy Jove Junco or El Sirio (The Syrian), as everyone calsl him, took advantage of the infinity of time to live. Do not laugh, as appearances are deceiving. This man is not crazy, he just sees life from a different perspective.</p>
<p>A man who makes incisions on reality by angles that we are not used to seeing, but no less successful. Stop for a moment at each virtual scenario that El Sirio is able to recreate in words and quite possibly you will find yourself looking at the world with different eyes.</p>
<p>Perhaps he is showing us a door not everyone is able to traverse, but that it leads to the paradise many religions speak of, where the time is experienced differently, there are no borders, there is no death and loneliness doesn&rsquo;t exist.</p>
<p>El Sirio is smarter than everyone because he beat frustration. He is, don&acute;t doubt it, a poet, novelist, journalist, gardener, Nobel Prize winner in medicine, orthopedic, polyglot, surgeon, lawyer, interpreter, anthropologist, philanthropist, lycanthrope, traveler of an ancient time, prisoner, millionaire, immigrant, migrant, emigrant, Ambassador, Dutch, Ethiopian, British, from Pinar del Rio (Cuban province), from Holguin (Cuban province), film critic, actor, film director, crazy, Syrian and artist. He erased each of his disappointments and replaced them with whatever he wanted. He is lucky. He stopped on the line between sanity and madness and he is aware of both conditions.</p>
<p>Sometimes, he has a 34.5% of madness combined with a 25% of sanity and the rest of freedom. Other times, this 25% of sanity becomes clarity, the 34.5% he transforms it into paranoia and the rest is caution. The formula varies. There may be a 27.5% of naivety mixed with 15.8% of tenderness, a 46% of crafty and the rest of sadness.</p>
<p>Art Brut Project Cuba and RIERA STUDIO are just one step in his upward artistic career. After years of being misunderstood, of prowling the vertical asphalt Alamar (town in the outskirts of Havana) imposes with its architecture, the artist has found the place he needed to be recognized.</p>
<p>So then, curators, sponsors, critics, psychologists, artists, writers, politicians and general public, the only thing you all can do is enjoy this opportunity and drink from this creative source.</p>
<span>* Maria de los Angeles Matienzo is writer and journalist.</span>
