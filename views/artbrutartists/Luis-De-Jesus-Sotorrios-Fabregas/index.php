			<div class="sectiontitle" id="artistssectiontitle">Luis de Jesus Sotorrios Fabregas  

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Havana, 1938  

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>At four years old, Luis de Jesus or Chucho as his friends call him, had several epileptic seizures. Since then, he constantly refers about the company of extraterrestrial beings who appear in his cognitive reality as visitors of worlds beyond our planet. Chucho hardly was able to finish 6th grade and he dropped the studies and he was devoted to help his parents with the crops of the farm where they lived. Chucho declares that he has been abducted in several occasions. In one occasion he woke up in an open field in the proximities of a nearby airport, while in another occasion he described how he was transported outside the Earth toward a place where he observed intelligent life, very similar to humans. Since 2000, Chucho begins to write on different paper supports, many of them of a considerable size. The stories are about the sightings he has perceived, scoring accurately their dates and hours, as compiled texts similar to the work of a medieval scribe. Occasionally, he also draws images of circles representing the moons of unknown planets. He also adds to his binnacle of extraterrestrial reports, personal essays on Roman numbers as if it were his own cryptographic language; as well as clinical-anatomical descriptions on illnesses, particularly on arterial hypertension. Chucho possess several suitcases with hundreds of his sightingÂ´s notes, which could become unique files of life presence in other worlds revealed to this simple and friendly man.<br/></p>
