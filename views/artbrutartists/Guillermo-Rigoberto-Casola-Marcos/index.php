			<div class="sectiontitle" id="artistssectiontitle">Guillermo Rigoberto Casola Marcos

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Havana, 1961

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Guillermo (Rigo) had attitudes toward drawing since a boy. He used to copy the drawings he saw in magazines and newspapers. Over time, he developed his abilities as a self-taught artist. He never had a specialized artistic training. During his high school studies, he simultaneously began to work with a group of decoration which was dedicated to repair and decorate the barber's shops and hairdresser’s salons of Havana. Later during his youth, he had others employments of diverse nature but in any case related to plastic arts. When he was about 30 years old and influenced by a friend, he took drawing more seriously although only as a way of personal satisfaction. First he began to carry out works on canvas with printing ink, works that he completely destroyed because of a psychotic crisis soon after a family problem. When he again began to draw, he started to carry out works in a way of comics’ strips on recycled papers that he picks up from different waste deposits and that he put together creating a kind of a manufactured magazine. Being a person that lives with humble conditions; he also spends his time to pick up plastic bottles, cans, broken electric appliances and any type of objects he finds outside trash cans that he later re-sell or use for his personal needs. This activity and attitude is well-known in Cuban in urban language as “tanquero” (Spanish word for: tanker man). The drawings that Rigo carries out are unique from its design and conception, completely far away from the logical sequence structure and the linear script as traditionally comics have. The use of the color as well as of the different graphic elements in each composition produces a final work of great visual force. In these graphic stories, Rigo reflects the evolution and problems of different real individuals from his environment as well as his own expectations and not achieved desires.<br/></p>
