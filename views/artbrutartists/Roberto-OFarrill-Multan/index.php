			<div class="sectiontitle" id="artistssectiontitle">Roberto O&rsquo;Farrill Multan 

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Ciego de Avila, 1961

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Roberto O'Farrill Multan was born with Down&rsquo;s syndrome. As a child he was always loved by his parents and siblings. The family made the decision not to send him to any special school as a way to protect him from the world outside his family. He began to draw as a way to express himself and as an alternative way of communication with the rest of the world. In his works, he proposes a series of imagined floor plans where he combines the frontal, lateral and zenithal angles of view to show us his particular and creative sight of different spaces of his real environment. The remarkable use of color in his drawings provides them with great visual force. Although he reflects the world that surrounds him through infantile simplicity drawings, the precise use of color and forms allow him to create a harmonic composition. In his drawings of rooms plans we can find architectural elements, as windows and doors, combined with dissimilar forms that represent his unique and characteristic vision of furniture, ornamental and house objects or electrical appliances that he arranges in a very personal way along the composition. Other times, his drawings represent large unique objects like all kinds of vehicles, pools and soccer fields. In some of the drawings he includes human figures that represent his family, friends and himself. Roberto also creates wooden sculptures. These objects, although simple in their conception and design, keep a close connection with his drawings, as in the structure and the color he uses. These pieces constitute an evolution of his work, from drawing toward a three-dimensional work.<br/>
</p>
