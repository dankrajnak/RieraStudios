			<div class="sectionbody" id="artistsectionbody">
			<p>Carlos is a Summa Cum Laude graduate from Mathematics. In 1990, his mother suddenly died while he was in Czechoslovakia (now the Czech Republic) for under graduate studies. During his stay abroad he began to present severe behavior dysfunctions mixed up with aggressive actions toward his school colleagues. His aggressive behavior became really dangerous causing his return to Cuba using a straitjacket and sedated. Once in Cuba, he was admitted in a psychiatric hospital and underwent to treatment with psychoactive drugs and electroshock sessions. He was diagnosed as paranoid schizophrenic. From that moment on, he has lived in a state of permanent alienation and he had been confined in psychiatric institutions several times. He has paranoid and distrusted attitudes, remaining in constant state of alert while facing the external world. Sometimes he presents aggressive behavior to well-known people and also unknown ones. Time after his diseases debuted, Carlos began drawing. He likes to use a palette of pastel colors on pieces of recycled cardboards boxes, old covers of vinyl disks, pieces of wooden tables and worn out pages of newspapers and magazines. In his drawings, he harmonizes the reality of the mathematics with personal imageries. He recreates a hybrid world of fellows who are always in his conscience; like angels or devils who swarm in his mind speaking to him. The human figure, frequently placed in foreground, it has the highest place in his representations. He also employs his own numeric alphabet, algorithms and words games as parables about his life, creating drawings from his abstract thoughts.<br/></p>
