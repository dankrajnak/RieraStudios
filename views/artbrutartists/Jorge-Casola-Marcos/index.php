			<div class="sectionbody" id="artistsectionbody">
			<p>Jorge (named Keke by his friends) suffered a car accident when he was ten years old. He was in coma for several months and although he began to recover later, his intellectual capacity was never the same. For that reason, he couldn&acute;t finish his studies. When he was 17 years old he debuted with psychiatric problems and he was diagnosed with schizophrenia paranoia. During the first stage of his illness he underwent electroshock therapy and later on he was prescribed with permanent psychoactive drugs. After his mother passed away, his father and brothers took care of him. His mental condition does not allow him to work so he depends completely on his family. Keke used to draw constantly since he was young and until his illness got worse and the heavy dose of psychoactive drugs didn&acute;t let him continue doing it. Nevertheless, he was motivated again and began drawing after several years of inactivity, also influenced by one of his brothers: Guillermo, who also begun drawing as a self-taught artist. Keke began drawing the &ldquo;disturbing dolls&rdquo;, as he calls the drawings of girls with big expressive blue eyes which are the constant motif of his artistic expression. This motif came from the obsessive representation of a forgotten and old doll found in a park and that he has in a bookshelf of his house. This doll could also be considered as a symbol of a woman, because it is the only female representation at home, so it was given the mother roll, among others.<br/>
