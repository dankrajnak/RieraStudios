			<div class="sectionbody" id="artistsectionbody">
			<p>Damian debuted with crisis of paranoia and hallucinations during adolescence. Since then, he was diagnosed as paranoid schizophrenic and he began treatments with different psychoactive drugs. He was admitted in two occasions in the Psychiatric Hospital of Havana due to the worsening of his paranoia crises and the developing of violent instincts. After time and the use of different treatments it was possible to significantly improve the aggressive aspects of his behavior, as well as to make him partly conscious of his illness. Since a child Damian was interested in drawing. He used to draw comics in his school&rsquo;s notebooks. When he was in his forties and observing wooden pieces, it came to his mind to assemble his first building model. He started making up structures of kind of factories or machines that later on, he intuitively put together to create models of future cities. Since then, Damian has been devoted exclusively and in an obsessive way to represent the cities that are built inside his mind. In times when wood was scarce, he began to build cities assembling all kinds of wasted objects which he gathered from the streets. In addition, he created all type of vehicles like cars, buses, airplanes, space ships, motorcycles, helicopters, ships, etc. Later, when the waste objects began to run out he decided to put all his imagination on the paper and he began to produce drawings and notebooks filled with images of his own cities. His drawings show us eclectic cities. He uses the perspective in such a unique and own way. Sometimes, the human presence becomes evident in some of his drawings of city scenes while in others are completely absence. In occasions, he shows us an atmosphere of calm and peace; but in other cases violence fills out all the paper with explicit popular riots and armed conflicts.<br/></p>
