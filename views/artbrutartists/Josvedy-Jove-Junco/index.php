			<div class="sectionbody" id="artistsectionbody">
			<p>Josvedy, nicknamed by his friends as El Sirio (Spanish expression for: The Syrian), was born under conditions of vital risk due to the arterial hypertension of his mother. After delivery, he was several days in an oxygen camera with convulsions. Since childhood he had difficulties in school, sudden changes of humor as well as paranoiac behaviors and hallucinations. However, he was able to graduate as a technician in accounting and he even registered for History and Philosophy career at the university although he quit it soon after he started. As an adult, his mother complained several times about his aggressive behavior. When her mother died, his father, a strong-willed, retired military man and in charge of a humble farm in the outskirts of Havana, began looking after him and he confirmed the impulsive aggressive behavior of Josvedy. It isn&rsquo;t known exactly when he began to draw and to write down in notebooks, with a perfect calligraphy and a unique and sometimes symbolic typography. He creates singular fantasy legends, transmuting any universal historical event and characters of real life to delivery his own version of the facts. He presents himself as the Prince of England, of Russian-Lithuanian origin, who knows more than 25 languages, winner of the Novel Prize in Orthopedic Sciences for the development of electromechanical legs for anatomic surgical operations; among other surprising events that he tells. His drawings contain fantastic characters and a great variety of graphic symbols, interspersed with the writings of his stories.<br/>
</p>
