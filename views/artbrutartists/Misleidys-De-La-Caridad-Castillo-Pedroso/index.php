			<div class="sectiontitle" id="artistssectiontitle">Misleidys de la Caridad Castillo Pedroso

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Mayabeque, 1985

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Misleidy was born in G&uuml;ines, a small town near Havana. During her first year of life and after several medical appointments with different specialists, she was diagnosed with severe bilateral sensorineural hearing loss, cerebral dysrhythmia and a disorder of autism spectrum. Hence, she is socially isolated beyond her family environment. Since childhood, her mother undertook her education instead of sending her to a hospital for mental illness patients due to the lack of an appropriate education center for her disability. It is unknown exactly when Misleidys began to draw large pieces of human figures; which then she cut them out and hung them on her room walls using scotch tape. A common feature to all these drawings is the representation of muscular men, with very expressive faces. They are posing, showing their strength as if they were bodybuilders and dressed only with underpants or even nudes. However, her creativity is such that there are no two figures that look alike. She also draws hefty women similar to her bodybuilder men, only wearing panties and braziers; as well as groups of several tied human figures in just one drawing. The artwork of Misleidys is a mystery for experts and psychologists. She has her own routine to draw and she is totally independent at each stage through the creation of her works. She uses and manipulates the paintbrush with a remarkable innate trust. The reason she began to draw this type of figures continues to be a mystery, even for her mother. She told us that sometimes Misleidys stands in front of her giant drawings with a particular look and expression, as if she were having some communication with them. Somehow we could say that these giants could be her protectors, rising up from her own spiritual world to take care of her and her small family.<br/>
</p>
