			<div class="sectionbody" id="artistsectionbody">
			<p>Lien was born with Down&rsquo;s syndrome. Since a little girl, she was very expressive and lovely. The warm of her family and specially, her comprehensive and supportive mother, motivated her to express herself through arts in spite of her special condition. Her mother became a great activist and collaborator in workshops for children with disabilities. Through these activities, Lien could interact with other children like her, and had totally freedom to develop her creativity. The most representative artwork that Lien creates is a drawing structured in the form of a letter, that she dedicates to her parents. The use of repeated sings following a specific pattern shows the creation of her very own alphabet. <br/></p>
