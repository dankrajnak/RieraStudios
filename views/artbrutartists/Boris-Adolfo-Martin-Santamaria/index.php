			</div>
		<div class="sectionsubtitle" id="artistssubsection">Havana, 1971

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>In his adolescence, Boris begun a life as a hippie and as a member of the Rock/Punk movement in the city. He started to have alcohol and all type of drugs abuse, as well as a high sexual promiscuity. When he was 29 years, he had serious problems of coexistence with his father, which added to his irreverent and unstable life, caused the definitive family rupture and he was pushed to abandon home. From that moment on, Boris begun to live in the street as a homeless. Since he was a boy, Boris used to draw skulls in his school notebooks, but without the intention of assuming drawing seriously. In 2006, he knew he was HIV positive, then he begun to draw constantly and obsessively to get rid of all his anger. His drawings often show faces of men, women and children transformed in diabolical, aggressive and ironic characters. In his draws, elements like blood and tears are compulsively represented. The political and social conflicts of his native Cuba, and the principal conflicts of the whole world, frequently are the central axis of his representations. Nowadays Boris is still living in the streets of Havana with an uncertain future. His irreverent behavior and a life full with conflicts, outrages, rebelliousness and bad habits keep him as an arid island amid the crowd.<br/></p>
