			<p>
				In his adolescence Boris frequently left his father's home beginning life as a hippie, and becoming part of the Rock/Punk movement with all that it brought: alcohol, drugs, psychoactive drugs, sexual promiscuity, etc. When he was 29 years old, Boris began having serious problems of coexistence with his father and his father's wife, which added to his irreverent and unstable life, caused the definitive family rupture and he was pushed to abandon home. From that moment on, Boris begun to live in the street as a homeless. Since he was a boy, Boris used to draw skulls in his school notebooks, but without the intention of assuming drawing seriously. In 2006, he knew he was HIV positive, then he begun to draw constantly and obsessively to get rid of all his anger. His drawings often show faces of men, women and children transformed in diabolical, aggressive and ironic characters. In his draws, elements like blood and tears are compulsively represented. The political and social conflicts of his native Cuba, as well as this globalized world, frequently are the central axis of his representations. Nowadays Boris is still living in the streets of Havana with an uncertain future. His irreverent behavior and a life full with conflicts, outrages, rebelliousness and bad habits keep him as an arid island amid the crowd.
