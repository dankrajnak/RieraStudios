			<div class="sectiontitle" id="artistssectiontitle">Luis Eusebio Coto

			</div>
		<div class="sectionsubtitle" id="artistssubsection">Havana, 1971

			</div>
			<div class="sectionbody" id="artistsectionbody">
			<p>Coto (as his friends call him) always has been interested in capturing beauty from another point of view. He creates fingers, hands and feet made of cement to which he inserts used and discarded acrylic nails that he found in the streets or any other place. In this way, he unconsciously makes a new interpretation of a disposable aesthetic beauty model. Admiring his works, and the wear, scratches, fractures and design of each nail he uses, it is inevitable to think about the owners of them: their physical appearance, personality, job, aesthetical preferences, etc. For Coto, perhaps this reality is simpler, fetishist or just he needs to understand something as complex and wonderful as women. In addition, as a tireless reader, one of his obsessions is to underline sentences and paragraphs through an incomprehensible methodology, proposing to a future reader the option to rethink or not the underline words. It is even possible to apply several reading methods: from a single and linear interpretation to a different reality built by hypertext. This could lead to thousands of places, many of them perhaps without meaning, but described with consistency as another reality from his unique vision. We can only say that Coto is a great enigma and he represents it in his work.<br/></p>

